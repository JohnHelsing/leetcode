    class Solution {
        public double[] sampleStats(int[] count) {
            return doublePoint(count);
        }

        public double[] doublePoint(int[] count) {
            double[] result = new double[5];//æœ€å°å€¼ã€æœ€å¤§å€¼ã€å¹³å‡å€¼ã€ä¸­ä½æ•°å’Œä¼—æ•°
            double max = 0;
            double min = 0;
            long sum = 0;
            double mid = 0;
            double maxN = 0;
            double total = 0;
            int maxNCount = 0;

            for (int i = 0; i < count.length; i++) {
                sum += i * 1.0 * count[i];
                if (count[i] > maxNCount) {
                    maxN = i;
                    maxNCount = count[i];
                }
                if (count[i] != 0) {
                    max = i;
                    total += count[i];
                }
                if (count[count.length - i - 1] != 0) {
                    min = count.length - i - 1;
                }
            }

            int tmp = 0;
            for (int i = 0; i < count.length; i++) {
                if (count[i] != 0) {
                    tmp += count[i];
                    if (tmp == total / 2) {
                        int j = i + 1;
                        while (count[j] == 0) {
                            j++;
                        }
                        mid = (i + j) / 2.0;
                        break;
                    }
                    if (tmp > total / 2) {
                        mid = i;
                        break;
                    }
                }
            }
            result[0] = min;
            result[1] = max;
            result[2] = sum / total;
            result[3] = mid;
            result[4] = maxN;
            return result;
        }
    }
