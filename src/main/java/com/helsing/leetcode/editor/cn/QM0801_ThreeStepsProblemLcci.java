            //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public int waysToStep(int n) {
            if (n <= 2) {
                return n;
            }
            return dp(n);
        }

        public int dp(int n) {
            int mod = 1000000007;
            int[] fi = new int[n + 1];
            fi[0] = 1;
            for (int i = 1; i <= n; i++) {
                fi[i] = fi[i - 1];
                if (i >= 2) {
                    fi[i] = (fi[i] + fi[i - 2]) % mod;
                }
                if (i >= 3) {
                    fi[i] = (fi[i] + fi[i - 3]) % mod;
                }
            }
            return fi[n];
        }
    }
